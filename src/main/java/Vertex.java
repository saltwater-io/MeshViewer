public class Vertex {
    float x, y, z;
    float nx, ny, nz;

    public Vertex(double x, double y, double z,double nx, double ny, double nz){
        this.x = (float)x;
        this.y = (float)y;
        this.z = (float)z;
        this.nx = (float)x;
        this.ny = (float)y;
        this.nz = (float)z;
    }

}
